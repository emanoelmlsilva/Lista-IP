num = int(input('Digite o número: '))

if(num % 2 != 0):
    print('Número é Impar')
else:
    print('Número não é Impar')
if(num % 3 == 0):
    print('Múltiplo de 3')
else:
    print('Número não é mútiplo de 3')
if(102 % num == 0):
    print('Divisor de 102')
else:
    print('Número não é divisor de 102')

